
public class DotElement {
	private double valeur;
	private int nbTour;
	
	public DotElement(double valeur, int nbTour) {
		this.valeur = valeur;
		this.nbTour = nbTour;
	}

	public double getValeur() {
		return valeur;
	}

	public void setValeur(double valeur) {
		this.valeur = valeur;
	}

	public int getNbTour() {
		return nbTour;
	}

	public void setNbTour(int nbTour) {
		this.nbTour = nbTour;
	}
	
	
}
